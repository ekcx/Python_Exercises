# More String Manipulation #

msg = "My First Message"
msg_second = "MY SECOND MESSAGE"
msg_third = "my third message"

if msg.isupper():
    print("UpperCase")
else:
    print("It is not upperCase")

if msg_second.isupper():
    print("UpperCase")
else:
    print("It is not upperCase")

if msg_third.islower():
    print("LowerCase")
else:
    print("It is not LowerCase")

